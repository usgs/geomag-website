<p>
  Dynamic events on the Sun’s surface can affect the Earth and technological systems of importance to modern society. The charged particles of the solar wind interact with Earth’s magnetic field and the electrically conducting parts of the Earth’s atmosphere. Usually, this interaction is relatively subdued during calm solar wind conditions, but every once in a while the Sun emits a burst of solar wind that causes a magnetic storm at Earth.
</p>
<p>
  Magnetic storms can wreak havoc on the electric power grids and communication lines. In September of 1859, a magnetic superstorm caused widespread interference to telegraph systems, even sparking fires at some telegraph stations [LINK]. In March of 1989 an intense magnetic storm caused a black-out across Québec; in November of 2001 another storm caused the failure of electrical power transmission in New Zealand, and yet another in October of 2003, called the Halloween storm, brought significant interference to the power-grid system in Sweden. Several studies, including one issued by the National Academy of Sciences [LINK], suggest that the future occurrence of a magnetic superstorm could cause widespread blackouts that would carry significant economic cost.
</p>

<h2>Geoelectric Hazards</h2>

<figure class='left'>
<img src="images/powerlines.jpg" alt="US at night from space showing lights"/>
<figcaption>
</figcaption>
</figure>


<p>
  A hazard is the probability that a dangerous natural phenomenon will occur with a certain size in a given location. So, for example, a 100-year geoelectric hazard is the amplitude of the geoelectric field that is only attained or exceeded at a given location once per century on average. The 100-year geoelectric hazard level is the standard used by electric utility companies for investigating the resilience of their systems to the effects of rare, but very intense magnetic storms. Maps of 100-year geoelectric hazards [LINK] assist utility company engineers as they examine which parts of distributed power-grid networks are more exposed, or, conversely, less exposed, to the deleterious effects of magnetic storms.
</p>
<p>
  The geoelectric hazard at any particular location depends on both the regional intensity of geomagnetic activity and on the electrical conductivity of the surrounding rock and deeper Earth material. To create a geoelectric hazard map, research scientists of the U.S. Geological Survey’s Geomagnetism Program [LINK] analyze historical records of magnetic storms acquired at long-running magnetic observatories, including those operated by the USGS across the continental United States. This analysis is then combined with magnetotelluric [LINK] measurements of the relationship between geomagnetic activity and the induced geoelectric field, such as those that have been at various geographic sites across the continental United States as part of the National Science Foundation’s EarthScope project [LINK].
</p>
<p>
  Recently, Geomagnetism Program scientists have concentrated on regional mapping of geoelectric hazards across the Pacific Northwest [LINK] and the Eastern United States [LINK]. This work shows that geoelectric hazards maps are closely correlated with geologic structures. In particular, during a magnetic storm, a location underlain with relatively conductive rock will allow currents to easily flow in the Earth in response to a weak geoelectric field. Conversely, the flow of a similar earth current under a location with more resistive rock requires a stronger geoelectric field. All of this means that, during a magnetic storm, electric currents, driven by geoelectric fields in the Earth, prefer to flow through ground connections at power-grid transformer stations and across transmission lines deployed across electrically resistive rock structures.
</p>
<h2>Voltages on Power Grids</h2>
<figure>
<img src="images/powerlines.v2.gif" alt=""/>
<figcaption>
  Schematic showing the generation of magnetic fields (B) of increasing intensity by a current (J_0) of increasing intensity in the space environment above the Earth’s surface. This magnetic field induces geoelectric fields (E) in the Earth, generally of high (low) amplitude within rock of low conductivity (high conductivity). These geoelectric fields, in turn, drive electric currents (J_1) that flow from the Earth, through ground connections at transformer substations and across high-voltage transmission lines.
</figcaption>
</figure>

<p>
  By integrating the geoelectric field induced during a magnetic storm along a power-grid line, one can estimate the voltage on the line. During an intense magnetic storm, hundreds, and even thousands, of volts can be generated on long power lines [LINK]. These voltages are not the carefully controlled alternating voltages provided by generating stations, and, indeed, geomagnetically induced voltages can drive uncontrolled currents on power grids that are of sufficient strength to damage transformers. If many transformers are damaged, as might happen during a magnetic superstorm, the result could be prolonged loss of electric power across a broad region of the United States.
</p>

<h2>Real-Time Geoelectric Maps</h2>
<p>
  An important ongoing project of the USGS and the National Oceanic and Atmospheric Administration is real-time mapping of geoelectric fields across the continental United States. To accomplish this, real-time maps of the geomagnetic field, created using data from magnetic observatories, are combined with magnetotelluric survey measurements to estimate the time-varying geoelectric field. These geoelectric field maps will assist utility companies with their real-time operations – enabling the assessment of geoelectric hazards as they evolve over the course of a magnetic storm and helping power-grid managers to make decisions that may minimize the impact to their systems.
</p>

