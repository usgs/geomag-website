<p>
  Electricity and the use of electricity for power and communication was still a relatively new technology in the late nineteenth century, so when strange things started happening on November 17, 1882 no one knew quite what to make of it. A few number of scientists had noticed the growing sunspot during the previous month, but at the time it had not yet been established that sun spots and solar flares were connected to magnetic disturbances on Earth and thus to the strange behavior of the newly-installed electric power and communication lines.
</p>
<p>
  The following day, newspapers reported people hearing a “buzzing, ringing noise” in the telephone lines, telegraph lines not working, a Western Union office that “ignited half a dozen times”, and even “melted” telegraph equipment. Spectacular aurora were visible over many night-times skies.  Even without Twitter or Instagram, stories about the odd events of the day quickly spread around the globe.
</p>

