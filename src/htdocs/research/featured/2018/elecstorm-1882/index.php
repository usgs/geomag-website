<p>
  Disturbances such as the November 1882 magnetic storm have occasionally occurred since then. Today, the U.S. Geological Survey monitors the Earth’s magnetic field using a network of 14 magnetic observatories that are situated across the United States and Territories. Data from these observatories enable scientists to understand magnetic storms and the hazard they represent for the operation of modern technological systems. Intense magnetic storms can disrupt radio signals, GPS systems used for navigation (think planes and boats), and power grids, sometimes causing wide-spread power outages.  Monitoring the sun and the earth’s geomagnetic signals allows us to prepare for potential disruptions and prevent potential disasters in the future.
</p>

