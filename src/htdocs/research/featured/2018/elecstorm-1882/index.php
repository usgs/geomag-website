<p>
  Some scientists believed that aurora and magnetic storms were somehow connected, but no one knew exactly what the connection was. We now know that they are caused by the interaction of the Earth with the sun. The Earth’s magnetic field is generated deep down in the Earth’s metallic core. This field imparts a force to the magnetized needles of compasses. The sun also has a magnetic field, one generated in its chaotic interior. The magnetic influence of the sun comes to the earth by way of an electrically conducting solar wind.
</p>
<p>
  Normally, this wind is something of a light breeze, but sunspots and solar flares can cause strong gusts of solar wind.  These bursts of energy interact with the earth’s magnetic field and can cause magnetic storms that interfere with electrical systems on the Earth, including power-lines and telephone and telegraph systems. Magnetic storms can also disturb simple compasses. We know about the November 1882 storm from historical accounts, and also from data recorded at a magnetic observatory that had just been installed in Los Angeles.
</p>

