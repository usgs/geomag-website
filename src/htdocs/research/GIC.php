<p>
  Geomagnetically-induced currents (GICs) flowing along electric
  power-transmission systems and other electrically-conducting infrastructure
  are produced by a naturally induced geo-electric field during geomagnetic
  disturbances.  An extreme example of this type of occurrence was the great
  magnetic storm of March 1989, which was one of the largest geomagnetic
  disturbances of the twentieth century. Rapid geomagnetic field variation
  during this storm led to the induction of electric currents in the Earth's
  crust. These currents caused wide-spread blackouts across the Canadian
  Hydro-Quebec power grid, resulting in the loss of electric power to more than
  6 million people (Allen, 1989; Czeck, 1992; Boteler, 1998; Kappenman, 1996,
  2003; Thomson, 2010). If a similar storm-induced blackout had occurred in the
  Northeastern United States, the economic impact could have exceeded $10
  billion (NRC, 2008; Baker et al., 2009; NRC report, 2009) not counting the
  negative impact on emergency services and the reduction in public safety
  associated with the loss of electric power in large cities.
</p>

<p>
  GIC levels are primarily driven by impulsive geomagnetic disturbances created
  by the interaction between the Earth's magnetosphere and sharp velocity,
  density, and magnetic field enhancements in the solar wind. These disturbances
  result in ground-level time varying magnetic fields, which, when they reach
  high levels, produce GICs. On average, 200 days of strong to severe
  geomagnetic storms that could produce GICs on the surface of the Earth can be
  expected during a typical 11-year solar cycle (DoE-NERC, 2010). However,
  knowing exact levels of induced currents in power grid infrastructure during a
  geomagnetic event requires knowledge of deep earth conductivities and
  transmission line design parameters (NERC, 2012).
</p>

