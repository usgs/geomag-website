<p>
  Reliable earthquake prediction is a worthwhile goal that, if ever attained,
  would reduce the loss of life and property. Unfortunately, it is not at all
  clear that earthquake prediction is either possible or practical, and the
  entire subject remains controversial. Because this subject is of societal
  importance, research staff of the USGS Geomagnetism Program have investigated
  published claims that geomagnetic and ionospheric signals associated with the
  earthquake process were measured prior to earthquake occurrence. So far, we
  have concluded that reported precursory signals are either bad data or the
  reported signals are part of normal global magnetic field variation that is
  unrelated to earthquakes.
</p>

