<h2>Demolition and Removal of a Seismic Vault at Sitka Magnetic Observatory</h2>

<h3>Introduction</h3>
<p>
  The United States Geological Survey maintains a geomagnetic observatory in
  Sitka, Alaska. This observatory is tasked with continually monitoring the
  magnetic field of the earth.
</p>
