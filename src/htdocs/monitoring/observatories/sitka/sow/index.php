<h3>Purpose and Need for Action</h3>
<p>
  The purpose and need for the proposed action is to remove an unsafe structure
  from the USGS Geomagnetic Observatory site in Sitka, Alaska while minimizing
  disturbance to natural resources in the area and negative effects to the
  historic nature of the site.  The structure is currently in use as a NOAA
  mission-critical seismic recording station but is an unsafe condition both for
  its current users and for persons using the surface above the structure.
</p>

<h3>Decision to be Made</h3>
<p>
  Based on the analysis in the Environmental Assessment, the USGS Responsible
  Official will determine if the preferred alternative will have a significant
  impact on the quality of the human environment.  If there is no significant
  impact, the USGS will issue a Finding of No Significant Impact (FONSI) and
  make a final decision regarding whether or not to proceed with the project on
  the basis of agency mission, legal mandates and public comment on the draft
  EA.
</p>
<p>
  If the Responsible Official determines that there may be a significant impact,
  the Official may choose to not proceed with the project or to prepare
  Environmental Impact Statement (EIS).
</p>

