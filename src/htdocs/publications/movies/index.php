<p>
  Movie-maps of low-latitude horizontal-intensity magnetic disturbance are
  derived from magnetic vector time series data collected at multiple
  ground-based observatories. Using a technique similar to that used in the
  calculation of Dst, a quiet time baseline is subtracted from the time series
  from each observatory. The remaining disturbance time series are shown in a
  polar coordinate system that accommodates both Earth rotation and the
  universal time dependence of magnetospheric disturbance. Each magnetic storm
  recorded in the movie-maps is different. While some standard interpretations
  about the storm time equatorial ring current appear to apply to certain
  moments and certain phases of some storms, the movie-maps also show
  substantial variety in the local time distribution of low-latitude magnetic
  disturbance, especially during storm commencements and storm main phases.
</p>

