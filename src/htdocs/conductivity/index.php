<h2>Regional Resistivity Models</h2>
<p>
  Geomagnetically induced currents (GIC) flowing along electric
  power-transmission systems are produced as a result of electromagnetic
  induction produced in the Earth by geomagnetic disturbances.  The
  depths into the planet to which the geomagnetic fields can penetrate
  are a function of the skin depth of Earth which can range from a few
  kilometers to 100's of km.  Therefore, to calculate the geoelectric
  field that drives GIC it is necessary to know how Earth's resistivity
  structure varies laterally and with depth. (P. Fernberg 2012,
  <a href="http://www.epri.com/abstracts/Pages/ProductAbstract.aspx?ProductId=000000000001026430">One-Dimensional
  Earth Resistivity Models for Selected Areas of Continental United States and
  Alaska</a>, EPRI Technical Update 1026430, Palo Alto, CA.)
</p>

